movieName = "Top Gun"

# string [incio:fim] - indice comeca no 0 e vai a final -1

# pegar toda string apartir da primeira posição

print (movieName[0:])

#buscar toda string até determinada posição

print (movieName[:5])

#buscar toda string apartir de determinada posicao 

print (movieName [2:])

"""
string [incio : fim : passo] 
passo vai determinar o incremento que por padrao e 1
"""

# buscar a string de 2 em 2 caracteres

print (movieName [::2])

# buscar tudo nos indices impares

print (movieName [1::2])

# inverter uma string 

print (movieName [::-1])